package examplethree;

public class Tripla {
	private String primo;
	private String secondo;
	private Double valore;
	public Tripla(String primo, String secondo, Double valore) {
		super();
		this.primo = primo;
		this.secondo = secondo;
		this.valore = valore;
	}
	public String getPrimo() {
		return primo;
	}
	public void setPrimo(String primo) {
		this.primo = primo;
	}
	public String getSecondo() {
		return secondo;
	}
	public void setSecondo(String secondo) {
		this.secondo = secondo;
	}
	public Double getValore() {
		return valore;
	}
	public void setValore(Double valore) {
		this.valore = valore;
	};
	

}
